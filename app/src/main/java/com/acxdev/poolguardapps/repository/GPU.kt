        val db = sqLiteZSelectTable(GPUDatabase::class.java)
        if(db.isEmpty()) gpuList().forEach {
            sqLiteZInsertInto(
                GPUDatabase::class.java,
                GPUDatabase(
                    it.model,
                    1,
                    0
                )
            )
        }
    }

